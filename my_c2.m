function c = my_c2(i, j)
% MY_C2 - 生成一个高级配色方案，根据两个输入参数生成相应的颜色。
% 输入：
%   i - 1到20之间的整数，用于选择基础颜色。
%   j - 1到6之间的整数，用于调节颜色的亮度。
% 输出：c - 表示颜色的1x3 double数组。

% 定义基础颜色序列
colors = [
    0.1216    0.4667    0.7059; % 暗蓝
    0.8392    0.1529    0.1569; % 红色
    0.0902    0.7451    0.8118; % 青色
    0.6510    0.4627    0.1137; % 棕色
    0.1725    0.6275    0.1725; % 绿色
    0.7373    0.7412    0.1333; % 黄色
    0.9569    0.4275    0.2627; % 橙色
    0.4       0.4       0.4;    % 暗灰
    0.9843    0.6039    0.6000; % 桃红色
    0.9255    0.8824    0.2;    % 淡黄色
    0.3010    0.7450    0.9330; % 淡蓝色
    0.6350    0.0780    0.1840; % 深红色
    0.9412    0.4706    0.4471; % 浅红
    0.9922    0.6824    0.3804; % 淡橙色
    0.5843    0.8157    0.9882; % 亮蓝色
    0.6941    0.3490    0.1569; % 棕红色
    0.5451    0.3490    0.2902; % 褐色
    0.8510    0.3294    0.6235; % 玫红色
    0.5961    0.8745    0.5412; % 浅绿
    0.6824    0.7804    0.9098; % 浅蓝
];

% 检查输入的数字是否为整数，并且在有效范围内
if ~isscalar(i) || i < 1 || i > 20 || floor(i) ~= i
    error('输入数字i必须是1到20之间的整数');
end
if ~isscalar(j) || j < 1 || j > 6 || floor(j) ~= j
    error('输入数字j必须是1到6之间的整数');
end


% 获取基础颜色
baseColor = colors(i, :);

% 计算调节系数
adjustmentFactor = 1 + 0.12* (j - 1); % 每次增加10%

% 调整颜色
c = baseColor * adjustmentFactor;

% 确保颜色值在0到1之间
c = min(c, 1);
end